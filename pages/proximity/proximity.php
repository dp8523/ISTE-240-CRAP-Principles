Proximity is in some ways a lot like alignment. They both help to establish hierarchy and decrease clutter. The major difference is that proximity is more specific to the elements that are already aligned. It is about placing elements into groupings and then spacing groupings to create a visually distinct hierarchy. For example if there is an image on a website, the text for that image would be right above or below it and keep close proximity, that way people can more easily view the content you are looking to convey. It can also help when grouping certain pieces of information together. If we were to group the major components of C.R.A.P. on the same page, you would want each letter in the acronym to be in its own dedicated section, not just thrown together in one massive paragraph as it would be easier to understand.
</p>
